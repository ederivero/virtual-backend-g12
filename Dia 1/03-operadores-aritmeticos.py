numero1 = 10
numero2 = 20

# SUMA +
print( numero1 + numero2) # 30
# RESTA - 
print( numero1 - numero2) # -10
# MULTIPLICACION *
print( numero1 * numero2) # 200
# DIVISION /
print(numero1 / numero2) # 0.5
# MODULO %
print(numero1 % numero2) # 10
# COCIENTE //
print(numero1 // numero2) # 0

